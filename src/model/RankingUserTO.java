package model;

/**
 *
 * @author THOR
 */
public class RankingUserTO {
    private String userName;
    private int time;

    public RankingUserTO(String userName, int time) {
        this.userName = userName;
        this.time = time;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    @Override
    public String toString() {
        return "{" + userName + " - " + time + '}';
    }
}
